    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String judul = txtJudul.getText().trim();
        String category = (String)cmbKategori.getSelectedItem();
        String ingredients = txtIngredients.getText().trim();
        String instructions = txtInstructions.getText().trim();
        boolean fav = chkFavorite.isSelected();

        if (judul.isEmpty() || ingredients.isEmpty() || instructions.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Judul, bahan, dan langkah wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            model.Recipe r = new model.Recipe(0, judul, ingredients, instructions, category, selectedImagePath, fav);
            controller.add(r);
            loadTableData();
            JOptionPane.showMessageDialog(this, "Resep berhasil ditambahkan!");
            // reset fields
            txtJudul.setText(""); txtIngredients.setText(""); txtInstructions.setText(""); chkFavorite.setSelected(false);
            lblImagePreview.setIcon(null); selectedImagePath = null;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah resep: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tblResepMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResepMouseClicked
        int row = tblResep.getSelectedRow();
        if (row >= 0) {
            int id = (int) tableModel.getValueAt(row, 0);
            try {
                // ambil detail dari DAO (atau gunakan controller.getAll lalu cari id)
                java.util.List<model.Recipe> list = controller.getAll();
                model.Recipe sel = null;
                for (model.Recipe rr : list) if (rr.getId() == id) { sel = rr; break; }
                if (sel != null) {
                    txtJudul.setText(sel.getTitle());
                    txtIngredients.setText(sel.getIngredients());
                    txtInstructions.setText(sel.getInstructions());
                    cmbKategori.setSelectedItem(sel.getCategory());
                    chkFavorite.setSelected(sel.isFavorite());
                    selectedImagePath = sel.getImagePath();
                    if (selectedImagePath != null) {
                        ImageIcon icon = new ImageIcon(new ImageIcon(selectedImagePath).getImage().getScaledInstance(
                            lblImagePreview.getWidth(), lblImagePreview.getHeight(), java.awt.Image.SCALE_SMOOTH));
                        lblImagePreview.setIcon(icon);
                    } else lblImagePreview.setIcon(null);

                    // aktifkan tombol ubah/hapus
                    btnUbah.setEnabled(true); btnHapus.setEnabled(true);
                }
            } catch (Exception e) { e.printStackTrace(); }
        }
    }//GEN-LAST:event_tblResepMouseClicked

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int row = tblResep.getSelectedRow();
        if (row < 0) { JOptionPane.showMessageDialog(this, "Pilih resep dulu!"); return; }
        int id = (int) tableModel.getValueAt(row, 0);
        String judul = txtJudul.getText().trim();
        String category = (String)cmbKategori.getSelectedItem();
        String ingredients = txtIngredients.getText().trim();
        String instructions = txtInstructions.getText().trim();
        boolean fav = chkFavorite.isSelected();

        if (judul.isEmpty() || ingredients.isEmpty() || instructions.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Judul, bahan, dan langkah wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            model.Recipe r = new model.Recipe(id, judul, ingredients, instructions, category, selectedImagePath, fav);
            controller.update(r);
            loadTableData();
            JOptionPane.showMessageDialog(this, "Resep berhasil diubah!");
            // reset & disable
            txtJudul.setText(""); txtIngredients.setText(""); txtInstructions.setText("");
            chkFavorite.setSelected(false); lblImagePreview.setIcon(null); selectedImagePath = null;
            btnUbah.setEnabled(false); btnHapus.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblResep.getSelectedRow();
        if (row < 0) { JOptionPane.showMessageDialog(this, "Pilih resep dulu!"); return; }
        int id = (int) tableModel.getValueAt(row, 0);
        int ans = JOptionPane.showConfirmDialog(this, "Hapus resep terpilih?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ans == JOptionPane.YES_OPTION) {
            try {
                controller.delete(id);
                loadTableData();
                JOptionPane.showMessageDialog(this, "Resep dihapus.");
                // reset input
                txtJudul.setText(""); txtIngredients.setText(""); txtInstructions.setText("");
                lblImagePreview.setIcon(null); selectedImagePath = null;
                btnUbah.setEnabled(false); btnHapus.setEnabled(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

